# Download and install the wazuh agent
#This will download the Windows version of the Wazuh agent. Check offical website for a different OS
#Using CMD run the below command:
wazuh-agent-4.11.2-1.msi /q WAZUH_MANAGER="MANAGER IP"

#Or you can run the below command using powershell on windows:
.\wazuh-agent-4.11.2-1.msi /q WAZUH_MANAGER="MANAGER IP"
